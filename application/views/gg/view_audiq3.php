<h2 class="sub">AUDI Q3</h2><img src="http://localhost/proyecto/img/audiq2.jpg" class="rounded mx-auto d-block"  width= 800/>


<p>La segunda generación del Audi Q3 llega al mercado a finales de 2018 para reemplazar al modelo que se vende con el mismo nombre desde el 2014. Esta renovación implica dimensiones más grandes tanto exteriores como interiores, mayor capacidad de carga, diseño retocado, gama de motores renovada y equipamiento más completo. Está a la venta desde 36.370 euros.

La marca de automóviles Audi pone a la venta a finales de 2018 la segunda generación del Q3, un SUV que llegó al mercado por primera vez en 2011. Este modelo, que para la firma alemana tiene una imagen mucho más deportiva, tiene una longitud de 4.485 milímetros (97 mm más), una anchura de 1.849 milímetros (18 mm más), una altura de 1.585 milímetros (5 mm más) y una distancia entre ejes de 2.680 milímetros (77 mm más). Este aumento de dimensiones proporciona, según la Audi, más espacio para las piernas y la cabeza de los pasajeros, así como más anchura entre puerta y puerta.

Su maletero también ha aumentado hasta llegar a los 530 litros (70 litros más que la versión anterior), y que puede llegar hasta los 675 litros si se desplaza longitudinalmente la segunda fila de asientos (hasta 150 mm). Estos asientos también son ajustables en inclinación. En caso de plegar los asientos, el volumen del maletero aumenta hasta los 1.525 litros (los respaldos están divididos en una proporción 40:20:40). El suelo del maletero puede ajustarse en tres niveles y el portón se puede abrir y cerrar con un movimiento de pie por debajo del paragolpes trasero. </p>

<img src="http://localhost/proyecto/img/audiq3.jpg" class="rounded mx-auto d-block"  width= 800/>

<p>El Audi Q3 2019 tiene como principales alternativas en el mercado el Volkswagen Tiguan (desde 27.310 €), el Volvo XC40 (desde 30.950 €), el Mercedes-Benz GLA (desde 31.400 €), el BMW X1 (desde 31.450 €), el Range Rover Evoque (desde 36.250 €) y el Jaguar E-Pace (desde 37.450 €). Este Audi está a la venta desde 35.920 euros con motor de gasolina y desde 37.390 con motor diésel.

La gama de motores con los que Audi comercializa este Q3 está compuesta por tres de gasolina (150, 190 y 230 CV) y dos diésel (150 y 190 CV). Estos motores pueden ir asociados a una caja de cambios manual de seis velocidades o a una automática de siete relaciones; la tracción puede ser a las ruedas delanteras o a las cuatro. Como opción, la marca ofrece al cliente el mando Audi drive select, con él que el conductor puede cambiar el comportamiento del coche con hasta en seis programas diferentes que modifican la respuesta del motor, la caja de cambios, la dirección y la suspensión.

Audi ofrece dos tipos de suspensiones, la normal y una más deportiva, que es opcional. Esta última cuenta con unos muelles y unos amortiguadores más duros. También está disponible con control electrónico de descenso de pendiente y llantas que pueden ir desde las 17 hasta las 20 pulgadas. </p>

