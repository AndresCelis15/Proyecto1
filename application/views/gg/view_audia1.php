<H1>El Audi A6 2018 llega con nuevos motores y un mayor equipamiento tecnológico</H1>
<div class=indi>
<h2 class="sub">AUDI A6</h2><img src="http://localhost/proyecto/img/audia2.jpg" class="rounded mx-auto d-block"  width= 800/>


<p> Audi pone a la venta desde 62 660 euros la nueva generación del Audi A6 2018. Este modelo cuenta con una renovada gama de motores, un interior más espacioso, diferentes mejoras en su dinámica y un mayor equipamiento. Dispone por primera vez de la tecnología Mild-Hybrid de 48 voltios, un sistema que permite reducir el consumo en 0,7 litros cada 100 kilómetros.

Audi presenta en marzo de 2018 la nueva generación del A6, que llega al mercado español en el arranque del segundo semestre de 2018 a un precio de 62 660 euros para su versión de lanzamiento con el motor diésel de 286 caballos. Por el momento, Audi solo ha difundido información de la carrocería berlina -la anterior generación contaba también con una familiar (Avant) y otra más campera (Allroad)-. Este nuevo modelo tiene una longitud de 4939 milímetros (7 mm más que su antecesor), una anchura de 1886 milímetros (12 mm mayor) y una altura de 1457 milímetros (2 mm mayor). Algunas de las alternativas que hay en el mercado las encontramos en el Jaguar XF (desde 43 060 €), el Volvo S90 (desde 44 911 €), el Mercedes-Benz Clase E (desde 48 700 €), el BMW Serie 5 (desde 49 400 €) o el Lexus GS (desde 53 700 €).</p>

<img src="http://localhost/proyecto/img/audia3.jpg" class="rounded mx-auto d-block"  width= 800/>

<p> Respecto al modelo anterior, Audi asegura que este A6 tiene más espacio para las piernas en la parte trasera, así como para la cabeza y los hombros tanto en la parte delantera como en la trasera. El maletero cuenta con más anchura de carga, pero la capacidad no varía y sigue manteniendo los 530 litros de carga. Tiene nuevos asientos que, opcionalmente, cuentan con ventilación y masaje. También son opcionales el techo corredizo panorámico, los faros HD Matrix LED, el sistema matricial de ledes, el cargador inalámbrico para móviles o las llantas de 21 pulgadas. Este modelo está a la venta en 15 colores, de los que un total de ocho son nuevos.

En el interior del coche puede haber hasta tres pantallas: una para la instrumentación, de 12,3 pulgadas; otra para la parte superior del salpicadero, de 8,8 pulgadas (opcional de 10,1”); y otra de 8,6 pulgadas (situada bajo la anterior) que permite controlar, entre otras funciones, el climatizador. Estas dos últimas pantallas se manejan a través del sistema operativo MMI touch response. </p>
