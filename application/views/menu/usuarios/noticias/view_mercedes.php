<h6 class ="text-center" > Mercedes-Benz Clase C ha sido presentado, el cambio de generación más esperado en los últimos meses ya fue dado a conocer y tenemos todos los detalles.</h6>

<br>

<p class="text-start">AUOTR: MARCO ALEGRÍA // FUENTE: www.publimetro.com.mx/mx</p>


<br>

<img style="padding: 20px;" src="http://localhost/proyecto/img/pipe.png" class="rounded float-start" width="700px" height="450px"/>
  
<p class="text-start">Tal como lo avisó, la mara dejará el 6 cilindros fuera para todas las versiones, ahora montará turbos de 4 cilindros y diferentes ayudas para mejorar el rendimiento, reducir las emisiones y ayudar al consumo.</p>
<br>
<p class= "text-start"> El bloque de entrada, liberado a nivel internacional, es un 2 litros de 4 cilindros turbo que libera 255 hp y 295 libras pie de torque, cifra que incrementa 22 lb-ft frente al modelo anterior. Y todo ello es gracias a un sistema de 48 voltios, conocido como Mild-Hybrid, que coopera con 20 h y 148 libras pie durante diferentes momentos para combinarse con el propulsor a gasolina y brindar arranques y paradas más suaves.</p>


<br>
<p class= "text-start"> Los tiempos de performance lo colocan en aceleraciones que rozan los 6 segundos en el 0-100. La transmisión es automática, la de 9 cambios que emplea la firma y la tracción es trasera, cuenta con una suspensión de nuevo diseño y suspensión independiente multi-enlace.</p>
<br>
<p class= "text-start"> A primera vista, Clase C no se ve significativamente diferente al anterior, pero los cambios están sutilmente disfrazados para brindar una continuidad a lo que encanta al público de Mercedes-Benz. En la parte delantera están faros son más afilados y la parrilla delantera ha sido incrementada en proporciones, siguiendo con el estilo que han dibujado desde la A a la S en los vehículos de la marca.</p>

<p class= "text-start"> En la parte posterior las luces traseras son más delgadas, pero se ensanchan a medida que van a los laterales. El cofre ofrece un par de líneas que realzan el volumen y la línea del techo baja de manera sutil para dibujar la silueta del sedán.   </p>


<p class= "text-start">  Derivado claramente de la nueva Clase S, con los paneles de instrumentos en cascada y la pantalla táctil central con orientación vertical. Porta salidas de aire diferenciadas frente a la Clase S, pero las pantallas son similares, más pequeñas, claro, que su hermano mayor. El cluster de instrumentos de 12,3 pulgadas y la pantalla de infoentretenimiento de 11,9 pulgadas, brindan un feeling tecnológico envidiable, el volante es mucho más deportivo y a nivel tecnológico luce actualizaciones inalámbricas, iluminación ambiental, múltiples temas de pantalla y un lector de huellas digitales para acceder a la configuración e información personal del vehículo.</p>

<p class= "text-start">  Sin lugar a dudas llegarán más modelos al line-up, seguramente un motor más pequeño, de entrada, para el mercado mexicano se sumará. Además de versiones más poderosas y las fantásticas AMG. Este modelo llegaría a México en el primer trimestre del año próximo.</p>

