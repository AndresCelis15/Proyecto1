            <p class="text-start">"Poner baterías enormes para miles de kilómetros, no estoy seguro de que sea una
                tendencia con futuro". Así se ha sincerado el CEO de Audi, Markus Duesmann, en una entrevista con la
                cabecera CNET en la que ha mostrado su visión de la industria, y del futuro eléctrico</p>
            <br>
            <p class="text-start"> Con el Audi e-tron marcando grandes cifras de ventas (sobre todo en Noruega) y la
                reciente presentación del bestial e-tron GT con alrededor de 488 km de autonomía, Duesmann reconoce que
                Tesla juega con ventaja en el terreno de la autonomía. </p>
            <br>
            <p class="text-start"> El CEO de la firma de los cuatro aros tiene claro que las sensaciones que permite el
                par instantáneo de los coches eléctricos son una definitiva baza a su favor, pero reconoce que es
                necesaria una gran inversión en baterías</p>
            <br>
            <p class="text-start"> En este escenario, en el que la autonomía juega un papel fundamental ante el
                progresivo -aunque a veces insuficiente- desarrollo de infraestructura de carga, Duessman cree que Tesla
                ha partido con ventaja en este aspecto: </p>
            <p class="text-start">"Gran parte de la industria comenzó con baterías mucho más pequeñas. Tesla comenzó con
                baterías enormes y vemos lo importante que es la autonomía para nuestros clientes".</p>
            <p class="text-start"> Sin embargo, no cree que en el futuro sea factible desarrollar coches con "baterías
                enormes para miles de kilómetros" ya que, explica, la infraestructura de carga se irá volviendo cada vez
                más densa, evitando la denominada ansiedad por la autonomía.</p>
            <p class="text-start"> "Creo que el tamaño de las baterías disminuirá de nuevo, porque hacen que los coches
                sean innecesariamente pesados ​​e innecesariamente caros. Y también innecesariamente grandes", ha dicho.
            </p>
            <p class="text-start"> También cree que el consumidor se irá adaptando al proceso de carga y a una
                experiencia distinta de la que ofrecen los coches de combustión, y eso favorecerá la democratización de
                coches eléctricos con baterías de menor tamaño. </p>
            <p class="text-start"> De momento, el Audi e-tron ha sido bien acogido en Europa, donde aumentó las ventas
                un 45 % en 2020, hasta las 26.454 unidades. Se queda en el noveno puesto de los 10 coches eléctricos más
                vendidos en este mercado durante el año pasado. </p>
