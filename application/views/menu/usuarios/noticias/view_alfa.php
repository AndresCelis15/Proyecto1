<h6 class ="text-center" > El futuro crossover de pequeño tamaño de Jeep no va emplear una de las plataformas de FCA como se había dicho inicialmente, sino la arquitectura CMP del antiguo Grupo PSA que también será empleada por los futuros B-SUV de Fiat y Alfa Romeo.</h6>

