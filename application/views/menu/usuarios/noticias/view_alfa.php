<p class="text-start">AUTOR: Sergio J. Cabrera // FECHA: 01/03/2021</p>

<br>

<img style="padding: 20px;"src="http://localhost/proyecto/img/romero.jpg" class="rounded float-start" width="650px" height="450px"/>
  
<p class="text-start">El nuevo B-SUV de Jeep compartirá la plataforma CMP con el Alfa Romeo Brennero. El futuro crossover de pequeño tamaño de Jeep no va emplear una de las plataformas de FCA como se había dicho inicialmente, sino la arquitectura CMP del antiguo Grupo PSA que también será empleada por los futuros B-SUV de Fiat y Alfa Romeo.</p>
<br>
<p class= "text-start"> Aunque pueda parecer un trabalenguas con tantas siglas entre medias, lo cierto es que es muy sencillo de entender. El futuro crossover de pequeño tamaño que se posicionará bajo el actual Jeep Renegade no hará uso de la plataforma esperada, que en principio debía proceder de FIAT, pues finalmente será la arquitectura flexible CMP que emplean Peugeot y Citroën.</p>
<br>
<p class= "text-start"> La fusión entre los grupos PSA y FCA va a ser un revulsivo sobre todo para las marcas europeas de esta última corporación, que van a poder aprovechar las más modernas arquitecturas de la compañía francesa para los segmentos más pequeños del mercado. Donde precisamente tenían una gran carencia, sobre todo en los modelos de las marcas más cercanas a la zona premium del mercado, como Alfa Romeo.</p>
<br>
<p class= "text-start"> Aunque desde hace varios meses sabemos que las firmas italianas del grupo van a hacer uso de la plataforma CMP francesa, hasta el reciente informe de Automotive News Europe no había salido a la palestra uno de los grandes olvidados de los últimos años, el futuro B-SUV o crossover de pequeño tamaño de Jeep. </p>



