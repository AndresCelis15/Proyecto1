<h4 class="text-center">BMW 220i Gran Coupé 2021, sedán deportivo con recarga de potencia</h4>

<h6 class ="text-center" > A pocos días de finalizar el 2020, la marca sorprende con la nueva versión de su sedán de entrada, que llega con más potencia, torque y equipamiento.</h6>

<br>

<p class="text-start">AUTOR: Héctor Siza Morales // FECHA: 23/12/2020</p>

<br>


<img style="padding: 20px;" src="http://localhost/proyecto/img/pipo.png" class="rounded float-start" width="700px" height="450px"/>
  
<p class="text-start"> Autogermana, el importador oficial de BMW, MINI y Motorrad para Colombia, mantuvo la dinámica de lanzamientos de nuevos modelos a lo largo del atípico 2020 y a menos de 10 días del cierre del año, sorprende con la introducción al mercado del nuevo BMW 220i Gran Coupé, un sedán con llamativo aspecto deportivo, que estrena el motor BMW TwinPower Turbo de 2 litros, cuatro cilindros, generador de 192 caballos de potencia y un torque de 280 Nm a 1.350 rpm, que le permiten acelerar de 0 a 100 km/h en 7,2 segundos y alcanzar una velocidad máxima de 240 km/h.   </p>
<br>
<p class= "text-start"> Estas cifras de potencia y velocidad, son logradas en virtud de la rápida entrega de torque y potencia, permitidas por la eficiente gestión de una transmisión Steptronic de 7 velocidades con doble embrague, y también por el diseño y fabricación basados en la aerodinámica y en el marco de un concepto de bajo peso, con el que se logró que el peso total del vehículo sea de 1.430 kilogramos.</p>

<br>
<p class= "text-start"> La característica sobresaliente del BMW Serie 2 Gran Coupé es su silueta alargada dinámicamente donde sobresalen sus cuatro puertas laterales sin marco. Este modelo que tiene 4.526 milímetros de largo, 1.800 milímetros de ancho y 1.420 milímetros de alto, a pesar de su aspecto deportivo y bajo, cuenta con generoso espacio para los pasajeros y carga, gracias a la distancia entre ejes de 2.670 milímetros. El baúl cuenta con capacidad de 430 litros y se puede ampliar abatiendo el respaldo del asiento trasero dividido 40/20/40.</p>


<p class= "text-start"> El sistema de información y entretenimiento del 220i Gran Coupé, está compuesto por una pantalla de 10.25 pulgadas para el panel de instrumentos y una central táctil de 10,25 pulgadas con interfaz Bluetooth para conexión de teléfono, llamadas y Streaming de música, que pueden ser manipulados por medio del asistente personal inteligente "Hey BMW!" que obedece a comandos de voz y puede aprender hábitos para aplicarlos en el contexto apropiado o proporcionar una conversación informal, incluso haciendo bromas ocasionales o relatando historias interesantes </p>

<p class= "text-start"> Entre los sistemas de asistencia al conductor también se encuentra el Parking Assistant, un asistente automático de parqueo horizontal y perpendicular, que maniobra, acelera, frena y opera los cambios sin la intervención del conductor. Si el anterior sistema sorprende, el nuevo asistente de reversa descresta, ya que memoriza los más recientes desplazamientos del vehículo siempre y cuando haya avanzado a no más de 36 km/h. Luego, el sistema puede conducir autónomamente el vehículo en reversa, hasta 50 metros a un máximo de 9 km/h, siguiendo exactamente el mismo recorrido que tomó al avanzar. </p>

