<h4 class="text-center">BMW Serie 5 2021, un sedán más elegante y deportivo cargado de tecnología</h4>

<h6 class ="text-center" > Esta es otra de las sorpresas que tenía preparadas BMW para la edición 2020 del Salón del Automóvil de Bogotá, que se estaría realizando por estas fechas.</h6>

<br>

<p class="text-start">AUTOR: Héctor Siza Morales // FECHA: 23/11/2020</p>

<br>

<img style="padding: 20px;" src="http://localhost/proyecto/img/5.jpg" class="rounded float-start" width="700px" height="450px"/>
  
<p class="text-start">Más imponente, pero sin dejar de lado la elegancia y el lujo, llega a Colombia el nuevo BMW  Serie 5, un sedán Premium que ahora cuenta con un interior lleno de detalles refinados y múltiples adelantos en sistemas de asistencia al conductor, control, operación y conectividad, que ubican a este modelo  cómo una opción deportiva, eficiente y progresiva e el segmento de la clase ejecutiva.</p>
<br>
<p class= "text-start"> Entre las nuevas características se encuentra un sistema de visualización y operación, que incluye una pantalla de 12,3 pulgadas y funciona gracias al Sistema Operativo 7 de BMW que brinda acceso a los servicios del Asistente Personal Inteligente de BMW.</p>
<br>
<p class= "text-start"> El diseño exterior del BMW Serie 5 proyecta deportividad e imponencia, gracias a elementos cómo la parrilla doble en forma de riñón que creció en tamaño y cae más abajo en el faldón delantero y lleva un marco de una sola pieza y combinan con el contorno más delgado de los faros, así como con las dos fuentes de luz de conducción diurna en forma de “U”, y opcionalmente en forma de “L” dispuestas una al lado de la otra, crean un gráfico de luz preciso y moderno.</p>
<br>
<p class= "text-start"> El sistema de iluminación ofrece de forma opcional unos nuevos faros LED completo que incluyen la función de curva adaptativa, luz de carretera BMW Selective Beam no deslumbrante con tecnología de matriz y el asistente de luz de carretera.</p>
<br>
<p class= "text-start"> El nuevo BMW Serie 5, en versión 530i, es impulsado por un motor dos litros a gasolina, generador de 249 hp de potencia y un torque de 350 Nm, conectado a una transmisión Steptronic de ocho velocidades, cuyo trabajo conjunto le permiten alcanzar una velocidad máxima de 250 km/h y acelerar de 0 a 100km/h en 6,1 segundos.</p>
<br>
<p class= "text-start"> El rango de funciones del Asistente de estacionamiento se ha ampliado con la adición del asistente de reversa, que toma el control de la dirección mientras retrocede para distancias de hasta 50 metros. Y la nueva visualización en 3D del área alrededor en el grupo de instrumentos proporciona una visión general mejorada de la situación actual del tráfico, el estado de los sistemas de asistencia al conductor y posibilidades de acción que ofrecen.</p>
<br>
<p class= "text-start">También se destaca el BMW Live Cockpit Professional con Sistema Operativo 7 de BMW, junto con una serie de servicios digitales adicionales. Además de calcular rutas y tiempos de llegada con precisión, y actualizar datos de tráfico en tiempo real a intervalos cortos, el nuevo sistema de navegación basado en la nube BMW Maps también permite ingresar cualquier palabra al seleccionar un destino. La función de serie de integración de teléfonos inteligentes ahora es compatible con Apple CarPlay y Android Auto. La actualización remota de software es otra característica nueva que permite importar funciones mejoradas del vehículo y servicios digitales adicionales en el automóvil por aire.</p>


